package com.example.helloworld.model;

public class OmsaetningElement {

    private String varenavn = "";
    private String afsaetning = "";
    private String salgspris = "";
    private String Omsaetning = "";

    public String getVarenavn() {
        return varenavn;
    }

    public void setVarenavn(String varenavn) {
        this.varenavn = varenavn;
    }

    public String getAfsaetning() {
        return afsaetning;
    }

    public void setAfsaetning(String afsaetning) {
        this.afsaetning = afsaetning;
    }

    public String getSalgspris() {
        return salgspris;
    }

    public void setSalgspris(String salgspris) {
        this.salgspris = salgspris;
    }

    public String getOmsaetning() {
        return Omsaetning;
    }

    public void setOmsaetning(String omsaetning) {
        Omsaetning = omsaetning;
    }
}
